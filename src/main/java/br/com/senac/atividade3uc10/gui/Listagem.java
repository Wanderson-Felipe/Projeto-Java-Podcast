    private void preencherTabela(String filtro) {
        DefaultTableModel tableModel = (DefaultTableModel) tblpodcast.getModel();
        tableModel.setRowCount(0);
        if (podcast != null) {

            for (Podcast podcast : podcast) {

                if (filtro.isEmpty() || podcast.getProdutor().toLowerCase().contains(filtro.toLowerCase())) {
                    Object[] rowData = {podcast.getId(), podcast.getProdutor(), podcast.getNomedoepisodio(), podcast.getNumeroepisodio(), podcast.getDuracao(), podcast.getUrl()};
                    tableModel.addRow(rowData);
                }
            }
        }
    }


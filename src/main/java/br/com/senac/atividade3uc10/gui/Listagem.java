    private void btnexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexcluirActionPerformed
        try {
            if (tblpodcast.getSelectedRow() >= 0) {
                int selectedRow = tblpodcast.getSelectedRow();
                Integer id = (Integer) tblpodcast.getValueAt(tblpodcast.getSelectedRow(), 0);
                String idString = id.toString();

                int resposta = JOptionPane.showConfirmDialog(this, "Deseja mesmo excluir o registro " + id + "?");
                if (resposta == 0) {

                    PodcastDAO podcastDao = new PodcastDAO();
                    podcastDao.excluir(id);
                    JOptionPane.showMessageDialog(this, "Registro excluído com sucesso");

                    DefaultTableModel model = (DefaultTableModel) tblpodcast.getModel();
                    model.removeRow(selectedRow);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());
        }

    }//GEN-LAST:event_btnexcluirActionPerformed

